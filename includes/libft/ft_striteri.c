#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t i;

	if (!s || !f)
	{
		return ;
	}
	i = -1;
	while (*s)
	{
		f(++i, s);
		s++;
	}
}
